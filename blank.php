c( 'influence_blog_site_layout_container_advance_b_d_m_top', function( value ) {
    value.bind( function( to ) {
        var $child = $( '.customizer-influence_blog_site_layout_container_advance_b_d_m_top' );
        if ( to ) {
            var style = '<style class="customizer-influence_blog_site_layout_container_advance_b_d_m_top">.container-style-boxed .primary-widget-area, .container-style-boxed .secondary-widget-area .widget, .container-style-content-boxed .primary-widget-area { padding-top: ' + to + 'px; }</style>';
            if ( $child.length ) {
                $child.replaceWith( style );
            } else {
                $( 'head' ).append( style );
            }
        } else {
            $child.remove();
        }
    } );
} );

